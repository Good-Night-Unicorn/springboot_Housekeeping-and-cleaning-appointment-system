	import { 
		isIntNumer,
		isMobile,
	} from "@/utils/validate";
	export default {
		data() {
			var validateMobile = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isMobile(value)) {
					callback(new Error("请输入正确的手机号码"));
				} else {
					callback();
				}
			};
			var validateIntNumber = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isIntNumer(value)) {
					callback(new Error("请输入整数"));
				} else {
					callback();
				}
			};
			return {
				id: '',
				type: '',
			
			
				ro:{
					dianpumingcheng : false,
					mima : false,
					dianpudizhi : false,
					fuwufanwei : false,
					lianxidianhua : false,
					yingyeshijian : false,
					yingyezhizhao : false,
					touxiang : false,
					sfsh : false,
					shhf : false,
					status : false,
				},
			
				ruleForm: {
					dianpumingcheng: '',
					mima: '',
					dianpudizhi: '',
					fuwufanwei: '',
					lianxidianhua: '',
					yingyeshijian: '',
					yingyezhizhao: '',
					touxiang: '',
					shhf: '',
				},

				rules: {
					dianpumingcheng: [
						{ required: true, message: '店铺名称不能为空', trigger: 'blur' },
					],
					mima: [
						{ required: true, message: '密码不能为空', trigger: 'blur' },
					],
					dianpudizhi: [
						{ required: true, message: '店铺地址不能为空', trigger: 'blur' },
					],
					fuwufanwei: [
						{ required: true, message: '服务范围不能为空', trigger: 'blur' },
					],
					lianxidianhua: [
						{ required: true, message: '联系电话不能为空', trigger: 'blur' },
						{ validator: validateMobile, trigger: 'blur' },
					],
					yingyeshijian: [
						{ required: true, message: '营业时间不能为空', trigger: 'blur' },
					],
					yingyezhizhao: [
						{ required: true, message: '营业执照不能为空', trigger: 'blur' },
					],
					touxiang: [
					],
					sfsh: [
					],
					shhf: [
					],
					status: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
				},
			};
		},
		props: ["parent"],
		computed: {



		},
		components: {
		},
		created() {
		},
		methods: {
			// 下载
			download(file){
				window.open(`${file}`)
			},
			// 初始化
			init(id,type) {
				if (id) {
					this.id = id;
					this.type = type;
				}
				if(this.type=='info'||this.type=='else'||this.type=='msg'){
					this.info(id);
				}else if(this.type=='logistics'){
					for(let x in this.ro) {
						this.ro[x] = true
					}
					this.logistics=false;
					this.info(id);
				}else if(this.type=='cross'){
					var obj = this.$storage.getObj('crossObj');
					for (var o in obj){
						if(o=='dianpumingcheng'){
							this.ruleForm.dianpumingcheng = obj[o];
							this.ro.dianpumingcheng = true;
							continue;
						}
						if(o=='mima'){
							this.ruleForm.mima = obj[o];
							this.ro.mima = true;
							continue;
						}
						if(o=='dianpudizhi'){
							this.ruleForm.dianpudizhi = obj[o];
							this.ro.dianpudizhi = true;
							continue;
						}
						if(o=='fuwufanwei'){
							this.ruleForm.fuwufanwei = obj[o];
							this.ro.fuwufanwei = true;
							continue;
						}
						if(o=='lianxidianhua'){
							this.ruleForm.lianxidianhua = obj[o];
							this.ro.lianxidianhua = true;
							continue;
						}
						if(o=='yingyeshijian'){
							this.ruleForm.yingyeshijian = obj[o];
							this.ro.yingyeshijian = true;
							continue;
						}
						if(o=='yingyezhizhao'){
							this.ruleForm.yingyezhizhao = obj[o];
							this.ro.yingyezhizhao = true;
							continue;
						}
						if(o=='touxiang'){
							this.ruleForm.touxiang = obj[o];
							this.ro.touxiang = true;
							continue;
						}
						if(o=='status'){
							this.ruleForm.status = obj[o];
							this.ro.status = true;
							continue;
						}
					}
				}
				// 获取用户信息
				this.$http({
					url: `${this.$storage.get('sessionTable')}/session`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						var json = data.data;
					} else {
						this.$message.error(data.msg);
					}
				});
			
			},
			// 多级联动参数

			info(id) {
				this.$http({
					url: `jiazhenggongsi/info/${id}`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						this.ruleForm = data.data;
						//解决前台上传图片后台不显示的问题
						let reg=new RegExp('../../../upload','g')//g代表全部
					} else {
						this.$message.error(data.msg);
					}
				});
			},

			// 提交
			async onSubmit() {
					if(this.ruleForm.yingyezhizhao!=null) {
						this.ruleForm.yingyezhizhao = this.ruleForm.yingyezhizhao.replace(new RegExp(this.$base.url,"g"),"");
					}
					if(this.ruleForm.touxiang!=null) {
						this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
					}
					var objcross = this.$storage.getObj('crossObj');
					await this.$refs["ruleForm"].validate(async valid => {
						if (valid) {
							if(this.type=='cross'){
								var statusColumnName = this.$storage.get('statusColumnName');
								var statusColumnValue = this.$storage.get('statusColumnValue');
								if(statusColumnName!='') {
									var obj = this.$storage.getObj('crossObj');
									if(statusColumnName && !statusColumnName.startsWith("[")) {
										for (var o in obj){
											if(o==statusColumnName){
												obj[o] = statusColumnValue;
											}
										}
										var table = this.$storage.get('crossTable');
										await this.$http({
											url: `${table}/update`,
											method: "post",
											data: obj
										}).then(({ data }) => {});
									}
								}
							}
							
							await this.$http({
								url: `jiazhenggongsi/${!this.ruleForm.id ? "save" : "update"}`,
								method: "post",
								data: this.ruleForm
							}).then(async ({ data }) => {
								if (data && data.code === 0) {
									this.$message({
										message: "操作成功",
										type: "success",
										duration: 1500,
										onClose: () => {
											this.parent.showFlag = true;
											this.parent.addOrUpdateFlag = false;
											this.parent.jiazhenggongsiCrossAddOrUpdateFlag = false;
											this.parent.search();
											this.parent.contentStyleChange();
										}
									});
								} else {
									this.$message.error(data.msg);
								}
							});
						}
					});
			},
			// 获取uuid
			getUUID () {
				return new Date().getTime();
			},
			// 返回
			back() {
				this.parent.showFlag = true;
				this.parent.addOrUpdateFlag = false;
				this.parent.jiazhenggongsiCrossAddOrUpdateFlag = false;
				this.parent.contentStyleChange();
			},
			yingyezhizhaoUploadChange(fileUrls) {
				this.ruleForm.yingyezhizhao = fileUrls;
			},
			touxiangUploadChange(fileUrls) {
				this.ruleForm.touxiang = fileUrls;
			},
		}
	};
</script>
